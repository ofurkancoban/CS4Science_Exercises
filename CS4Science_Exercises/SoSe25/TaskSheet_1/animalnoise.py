user_input = input("Enter your animal's name: ")

if user_input  == "dog":
    print("Wuff!")
elif user_input == "cat":
    print("Meow!")
elif user_input == "bird":
    print("Piep!")
elif user_input == "cow":
    print("Moo!")
elif user_input == "sheep":
    print("Mee!")
